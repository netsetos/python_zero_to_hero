# python
# Bitwise Operators
a = 5  # Binary: 0101
b = 3  # Binary: 0011

print(a & b)    # AND: 1 (Binary: 0001)
print(a | b)    # OR: 7 (Binary: 0111)
print(a ^ b)    # XOR: 6 (Binary: 0110)
print(~a)       # NOT: -6 (Flips bits)
print(a << 1)   # Left shift: 10 (Binary: 1010)
print(a >> 1)   # Right shift: 2 (Binary: 0010)